    public function get_taobao_tlk($skuId,$set = false){
        global $_GPC, $_W;

//        $keyword = "官方授权英国illamasqua限量色高光粉盘偏光Deity陈橘大推荐珠光";
//        $skuId = "558644332609";

        if(!$set){
            $set = m("fc")->fc_getglobal();
        }


        $c = new TopClient;
        $c->appkey = $set['taobao_app_key'];
        $c->secretKey = $set['taobao_app_secret'];

        $coupon = $this->goods_coupon($skuId,$set);

        if(array_key_exists('sub_code',$coupon)){
            return result_data(1,'获取优惠券失败',$coupon);
        }

        $goods = $this->goods_detail($skuId , $set);


        $req = new TbkTpwdCreateRequest();
        $req->setUrl($coupon['coupon_click_url']);
        $req->setText($goods['data']['skuName']);
        $req->setLogo($goods['data']['picUrl']);

        $resp = $c->execute($req);
        $resp = json_decode(json_encode($resp), true);

        if(!array_key_exists('data',$resp)){
            return result_data(2,'获取淘口令失败',$coupon);
        }
        $model_response = $resp['data']['model'];
        $coupon['tbk_pwd'] = $model_response;

        return result_data(0,'获取淘口令成功',$coupon);
    }



