    //淘宝订单列表字段转换
    function order_list_field_convert($pdd_data,$set = false){

//        通过订单号后6位查询淘宝id
        $trade_id = $pdd_data['trade_id'];
        $taobao_user_id = substr($trade_id,-6);
        $user_str1 = substr($taobao_user_id,0,2);
        $user_str2 = substr($taobao_user_id,2,2);
        $user_str3 = substr($taobao_user_id,4,2);
        $taobao_user_id = $user_str1 . $user_str3 .$user_str2;

        $uid = pdo_fetchcolumn("select uid from ".tablename("qt_operate_member")." where taobao_user_id=:taobao_user_id",[":taobao_user_id"=>$taobao_user_id]);

        $goods = $this->goods_detail($pdd_data['num_iid'] , $set);

        $order = [];
        $order['ordertype'] = 4;
        $order['orderId'] = $pdd_data['trade_id'];
        $order['orderTime'] = strtotime($pdd_data['create_time']);//创建时间
        $order['finishTime'] = '';
        $order['positionId'] = empty($uid) ? 0 : $uid;
        $order['skuId'] = $pdd_data['num_iid'];
        $order['skuName'] = $pdd_data['item_title'];
        $order['picUrl'] = empty($goods['data']['picUrl']) ? '' : $goods['data']['picUrl'];//********还没弄
        $order['skuNum'] =$pdd_data['item_num'];
        $order['cosPrice'] = doubleval($pdd_data['alipay_total_price']);
        $order['commissionRate'] = $pdd_data['commission_rate'] * $pdd_data['income_rate'] * 100;
        $order['commission'] = $pdd_data['alipay_total_price'] * $order['commissionRate'] /100;
        $order['Fee'] =  $order['commission'];
        $order['finalRate'] = $order['commissionRate'];

        if($pdd_data['tk_status'] == 3){
            $order['valistatus'] = "订单结算";
        }
        if($pdd_data['tk_status'] == 12){
            $order['valistatus'] = "订单付款";
        }
        if($pdd_data['tk_status'] == 13){
            $order['valistatus'] = "订单失效";
        }
        if($pdd_data['tk_status'] == 14){
            $order['valistatus'] = "订单成功";
        }

        $order['validCode'] = $pdd_data['tk_status'];//**********还没弄

        return $order;
    }

