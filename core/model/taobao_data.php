    public function goods_search($set=false){

        global $_GPC, $_W;

        if(!$set){
            $set = m("fc")->fc_getglobal();
        }

        $c = new TopClient;
        $c->appkey = $set['taobao_app_key'];
        $c->secretKey = $set['taobao_app_secret'];
        $pid = explode("_", $set["taobao_pid"]);

        $adzone_id = $pid["3"];
        $req = new TbkDgMaterialOptionalRequest;
        $req->setAdzoneId($adzone_id);


        if(empty($_GPC['keyword']) && empty($_GPC['cid'])){
            $cate = pdo_fetch("select id,name,pdd_cid,jd_cid,tb_cid from " . tablename("qt_classification")." where parent_id=0 and is_delete=0 and uniacid=:uniacid ORDER BY sort DESC",['uniacid'=>$_W['uniacid']]);
            if($cate){
                $_GPC['cid'] = $cate['tb_cid'];
            }
        }

        if(!empty($_GPC["mincommission"])){
            $mincommission = $_GPC["mincommission"] * 100;
            $req->setStartTkRate($mincommission);
        }
        if(!empty($_GPC["maxcommission"])){
            $maxcommission = $_GPC["maxcommission"] * 100;
            $req->setEndTkRate($maxcommission);
        }

        if(!empty($_GPC["minprice"])){
            $req->setStartPrice(intval($_GPC["minprice"]));
        }
        if(!empty($_GPC["maxprice"])){
            $req->setEndPrice(intval($_GPC["maxprice"]));
        }

        $req->setHasCoupon( $set['is_open_coupon'] == 1 ? 'true' : 'false');


        if(!empty($_GPC['keyword'])){
            $req->setQ($_GPC['keyword']);
        }
        if(!empty($_GPC['cid'])){
            $req->setCat($_GPC['cid']);
        }

        $req->setPageNo($_GPC["page"]);


        $resp = $c->execute($req);
        $resp = json_decode(json_encode($resp), true);

        if(!array_key_exists('result_list',$resp)){
            return false;
        }
        $goods_detail_response = $resp['result_list']['map_data'];

        $goods_list = [];
        if($resp['total_results'] == '1'){
            $goods_list[] = $this->goods_list_field_convert($goods_detail_response); // 只有一条的时候不返回数组
        }else{
            foreach ($goods_detail_response as $key =>$val){
                $goods_list[] = $this->goods_list_field_convert($val);
            }
        }


        $result['data'] = $goods_list;
        $result['totalpage'] = intval($resp['total_results']/20);
        $result['total'] = $resp['total_results'];
        return $result;
    }

